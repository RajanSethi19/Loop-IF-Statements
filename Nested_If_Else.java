package Loop_and_Ifstatements;

public class Nested_If_Else {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;

		if (a > b) {

			if (a > 5) {
				System.out.println("a is greater than 5");

			}

			else {

				System.out.println("a is smaller than 5");
			}

		}

		else {
			System.out.println("b is greater");

		}

	}

}
