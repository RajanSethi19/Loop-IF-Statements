package Loop_and_Ifstatements;

public class Many_If {

	public static void main(String[] args) {
		int a = 200;
		int b = 400;
		if (a > b) {//false
			System.out.println("a is greater");
		}
		if (a == 200) {//true
			System.out.println("a == 200");
		}
		if (b == 400) {//true
			System.out.println("b ==400");
		}
		if (b > a) {//true
			System.out.println("a is greater");
		}

	}

}
